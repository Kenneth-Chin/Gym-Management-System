    private void loginButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButActionPerformed
        Trainer tr_acc = null;
        for (Trainer tr: APUGym.getTrainers()){
            if(tr.verifyUserLogin(trainerUsernameTxt.getText(), trainerPasswordTxt.getText())){
                tr_acc = tr;
                break;
            }
        }
        if(tr_acc == null){
            JOptionPane.showMessageDialog(null, "Wrong username or password. Please try again.", "Trainer Login", JOptionPane.WARNING_MESSAGE);
        } else{
            new trainerMenuForm(APUGym, tr_acc).setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_loginButActionPerformed

