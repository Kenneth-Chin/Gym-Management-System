    private void proceedPaymentButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proceedPaymentButActionPerformed
        // TODO add your handling code here:
        if(cust_acc.getCustomerSubscriptionPlan().equals("Free")){
            ArrayList<Payment> Payments = APUGym.getPayments();
            String lastPaymentID = Payments.get(Payments.size() - 1).getPaymentID();
            lastPaymentID = lastPaymentID.replace("PA","");
            int num = Integer.parseInt(lastPaymentID);
            num++;
            if(num<10){
                lastPaymentID = "PA00"+Integer.toString(num);
            }else if(num>=10 || num<100){
                lastPaymentID = "PA0"+Integer.toString(num);
            }else{
                lastPaymentID = "PA"+Integer.toString(num); 
            }
            Payment subscriptionPayment = new Payment(lastPaymentID, 
                    chooseSubscriptionPlan.getSelectedItem().toString() + " Subscription Plan", 
                    "-", 
                    Double.parseDouble(totalAmount.getText().split(" ")[1]), 
                    cust_acc.getUserID(), 
                    "MA001", 
                    LocalDateTime.now(), 
                    "Pending");
            APUGym.getPayments().add(subscriptionPayment);
            new customerPaymentMethodForm(APUGym, cust_acc, subscriptionPayment).setVisible(true);
            this.dispose();
        } else{
            JOptionPane.showMessageDialog(null, "You are already subscribed!", "Unable to proceed",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_proceedPaymentButActionPerformed

    private void chooseSubscriptionPlanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chooseSubscriptionPlanActionPerformed
        // TODO add your handling code here:
        String selectedPlan = chooseSubscriptionPlan.getSelectedItem().toString();
        if(selectedPlan.equals("Basic")){
            totalAmount.setText("RM " + String.format("%.2f", (double) 100 * 3));
        } else if(selectedPlan.equals("Standard")){
            totalAmount.setText("RM " + String.format("%.2f", (double) 80 * 6));
        } else if(selectedPlan.equals("Elite")){
            totalAmount.setText("RM " + String.format("%.2f", (double) 60 * 12));
        }
    }//GEN-LAST:event_chooseSubscriptionPlanActionPerformed

