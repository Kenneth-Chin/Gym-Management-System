    private void loginButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButActionPerformed
        Customer cust_acc = null;
        for (Customer cust: APUGym.getCustomers()){
            if(cust.verifyUserLogin(customerUsernameTxt.getText(), customerPasswordTxt.getText())){
                cust_acc = cust;
                break;
            }
        }
        if(cust_acc == null){
            JOptionPane.showMessageDialog(null, "Wrong username or password. Please try again.", "Customer Login", JOptionPane.WARNING_MESSAGE);
        } else{
            new customerMenuForm(APUGym, cust_acc).setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_loginButActionPerformed

